package com.curso.springmongo.user;

public class User {
	private String name;
	private String genre;
	private int age;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGenre() {
		return genre;
	}
	public void setGenre(String genre) {
		this.genre = genre;
	}
	public int getEdad() {
		return age;
	}
	public void setEdad(int age) {
		this.age = age;
	}
	
	
}
